from turtle import Turtle


class Paddle(Turtle):

    def __init__(self, position):
        super().__init__()

        # Step 2 creating paddle
        # paddle = Turtle()
        self.shape("square")
        self.color("white")
        self.shapesize(stretch_wid=5, stretch_len=1)
        self.penup()
        self.goto(position)

    # Step 3 movement of paddle
    def move_up(self):
        new_y = self.ycor() + 20
        self.goto(self.xcor(), new_y)

    def move_down(self):
        new_y = self.ycor() - 20
        self.goto(self.xcor(), new_y)






